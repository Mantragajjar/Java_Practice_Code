public class Static {
    static void div()
    {
        int a=10;
        int b=5;
        System.out.println("div is:"+(a/b));
    }
    static void mul()
    {
        int c=10;
        int d=5;
        System.out.println("multiplication is :"+(c*d));
    }
    public static void main (String ar[])
    {
        div();
        mul();
    }
}
